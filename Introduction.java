public class Introduction{ // introduction is the file name

    public static void main(String[] arg){
        System.out.print("Hello world");  // unlike python, java ended with ;
        System.out.print("Hello again"); // the problem with this is it still print in the same line, unlike python
        /*  To fix this is simple we can use:
            - println
            - \n inside the print statement like python
        */


        // println
        System.out.println("Hello World");
        System.out.println("Hello World again"); // notice that println needed to use for all of the print statement?

        // \n
        System.out.print("Hello \n");
        System.out.print("Again"); // while \n use print instead of println


    }

}